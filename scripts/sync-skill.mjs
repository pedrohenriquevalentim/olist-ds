import { readdirSync, readFileSync, writeFileSync, statSync, existsSync } from 'fs';
import { join } from 'path';

const COMPONENTS_DIR = './src/components';
const SKILL_DIR = './.claude/skills/olist-ds-skill';
const VARIABLES_PATH = './src/generated/variables.css';

// ‚îÄ‚îÄ‚îÄ 1. Atualiza COMPONENTS.md ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

function syncComponents() {
  const dirs = readdirSync(COMPONENTS_DIR)
    .filter(name => {
      const fullPath = join(COMPONENTS_DIR, name);
      if (!statSync(fullPath).isDirectory()) return false;
      return existsSync(join(fullPath, `${name}.tsx`));
    });

  const components = dirs.map(name => {
    const tsxPath = join(COMPONENTS_DIR, name, `${name}.tsx`);
    const cssPath = join(COMPONENTS_DIR, name, `${name}.module.css`);
    const source = readFileSync(tsxPath, 'utf-8');

    // Extrai interface de props
    const propsMatch = source.match(/interface\s+(\w+Props)\s*\{([^}]+)\}/s);
    const propsBlock = propsMatch ? propsMatch[0] : 'Props n√£o encontradas';

    // Extrai variantes de props (ex: 'primary' | 'secondary')
    const variants = [];
    const variantMatches = source.matchAll(/'([^']+)'/g);
    for (const m of variantMatches) {
      if (!variants.includes(m[1]) && m[1].length < 30) {
        variants.push(m[1]);
      }
    }

    // Verifica quais arquivos existem
    const hasTest = existsSync(join(COMPONENTS_DIR, name, `${name}.test.tsx`));
    const hasStory = existsSync(join(COMPONENTS_DIR, name, `${name}.stories.tsx`));
    const hasCss = existsSync(cssPath);

    // Extrai tokens CSS usados
    let tokensUsed = [];
    if (hasCss) {
      const css = readFileSync(cssPath, 'utf-8');
      const tokenMatches = css.matchAll(/var\(--([^)]+)\)/g);
      for (const m of tokenMatches) {
        if (!tokensUsed.includes(m[1])) {
          tokensUsed.push(m[1]);
        }
      }
    }

    return { name, propsBlock, variants, hasTest, hasStory, hasCss, tokensUsed };
  });

  // Gera o markdown
  let md = `# Components\n\n`;
  md += `## Available Components (${components.length})\n\n`;
  md += `> Auto-generated by sync-skill.mjs ‚Äî do NOT edit manually.\n`;
  md += `> Last updated: ${new Date().toISOString().split('T')[0]}\n\n`;

  for (const comp of components) {
    md += `### ${comp.name}\n\n`;
    md += `**Import:** \`import { ${comp.name} } from '@pedrohenriquevalentim/olist-ds';\`\n\n`;
    md += `**Files:** `;
    md += `tsx ‚úÖ `;
    md += comp.hasCss ? `css ‚úÖ ` : `css ‚ùå `;
    md += comp.hasTest ? `test ‚úÖ ` : `test ‚ùå `;
    md += comp.hasStory ? `story ‚úÖ` : `story ‚ùå`;
    md += `\n\n`;

    md += `**Props:**\n\`\`\`tsx\n${comp.propsBlock}\n\`\`\`\n\n`;

    if (comp.tokensUsed.length > 0) {
      md += `**Tokens used:** `;
      md += comp.tokensUsed.slice(0, 15).map(t => `\`--${t}\``).join(', ');
      if (comp.tokensUsed.length > 15) md += ` (+${comp.tokensUsed.length - 15} more)`;
      md += `\n\n`;
    }

    md += `---\n\n`;
  }

  // Adiciona se√ß√£o de componentes planejados
  md += `## Component Creation Rules\n\n`;
  md += `When building a new component:\n\n`;
  md += `1. Create in \`src/components/ComponentName/\` with all files (tsx, css, test, stories, index)\n`;
  md += `2. Use ONLY tokens from \`src/generated/variables.css\`\n`;
  md += `3. Export with named export (not default)\n`;
  md += `4. Props typed with \`interface\`\n`;
  md += `5. Include all states: default, hover, focus, active, disabled\n`;
  md += `6. Add ARIA attributes for accessibility\n`;
  md += `7. Use CSS Modules (\`.module.css\`)\n`;
  md += `8. After creation, run \`npm run build\` to update this skill automatically\n`;

  writeFileSync(join(SKILL_DIR, 'references', 'COMPONENTS.md'), md);
  console.log(`  ‚úÖ COMPONENTS.md ‚Äî ${components.length} components`);

  return components;
}

// ‚îÄ‚îÄ‚îÄ 2. Atualiza SOURCE_MAP.md ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

