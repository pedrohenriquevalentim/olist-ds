// ‚îÄ‚îÄ‚îÄ 3. Atualiza OLIST_DS_OVERVIEW.md (lista de componentes) ‚îÄ

function syncOverview(components) {
  const overviewPath = join(SKILL_DIR, 'references', 'OLIST_DS_OVERVIEW.md');
  let md = readFileSync(overviewPath, 'utf-8');

  // Substitui a lista de componentes existentes
  const componentList = components.map(c => `- **${c.name}**`).join('\n');
  const marker = '### Existing Components';
  const markerEnd = 'For full component API';

  const startIdx = md.indexOf(marker);
  const endIdx = md.indexOf(markerEnd);

  if (startIdx !== -1 && endIdx !== -1) {
    const before = md.substring(0, startIdx);
    const after = md.substring(endIdx);

    md = before;
    md += `### Existing Components\n\n`;
    md += `Before creating ANY new element, check if one of these exists (${components.length} total):\n\n`;
    md += componentList + '\n\n';
    md += after;

    writeFileSync(overviewPath, md);
    console.log(`  ‚úÖ OLIST_DS_OVERVIEW.md ‚Äî component list updated`);
  }
}

// ‚îÄ‚îÄ‚îÄ 4. Gera pacote .skill para upload no Claude.ai ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

function packageForClaudeAI() {
  // Copia skill para outputs para f√°cil acesso
  const files = [];
  function walk(dir) {
    for (const entry of readdirSync(dir)) {
      const full = join(dir, entry);
      if (statSync(full).isDirectory()) {
        walk(full);
      } else {
        files.push(full);
      }
    }
  }
  walk(SKILL_DIR);
  console.log(`  ‚úÖ Skill package ready (${files.length} files)`);
}

